import csv
import os


class ExperimentTracker:
    """
    Records completed games and exports results for the assignment report.

    Usage
    -----
    tracker = ExperimentTracker()
    tracker.record(
        algo='alphabeta', depth=4, length=16, starter='human',
        result='first', nodes=1243, move_times=[120.3, 98.7, 134.2],
        points=7, bank=2
    )
    tracker.export_csv('results.csv')
    """

    def __init__(self) -> None:
        self.games: list[dict[str, object]] = []

    def record(
        self,
        algo:       str,
        depth:      int,
        length:     int,
        starter:    str,
        result:     str,
        nodes:      int,
        move_times: list[float],
        points:     int,
        bank:       int,
    ) -> None:
        """
        Record one completed game.

        Parameters
        ----------
        algo        : 'minimax' or 'alphabeta'
        depth       : search depth used
        length      : initial string length
        starter     : 'human' or 'computer'
        result      : 'first', 'second', or 'draw'
        nodes       : total nodes generated by the computer this game
        move_times  : time in ms for each computer move
        points      : final points value
        bank        : final bank value
        """
        self.games.append({
            'game':    len(self.games) + 1,
            'algo':    algo,
            'depth':   depth,
            'length':  length,
            'starter': starter,
            'result':  result,
            'nodes':   nodes,
            'avg_ms':  self.average_time(move_times),
            'points':  points,
            'bank':    bank,
        })

    def average_time(self, move_times: list[float]) -> float:
        """
        Return average move time in ms, or 0.0 if no moves recorded.

        Parameters
        ----------
        move_times : list of individual move durations in ms

        Returns
        -------
        float : average duration rounded to 2 decimal places
        """
        if not move_times:
            return 0.0
        return round(sum(move_times) / len(move_times), 2)

    def summary(self) -> dict[str, dict[str, int]]:
        """
        Return win/loss/draw counts broken down by algorithm.

        Returns
        -------
        dict : {
            'minimax':   {'first': int, 'second': int, 'draw': int},
            'alphabeta': {'first': int, 'second': int, 'draw': int},
            'total':     {'first': int, 'second': int, 'draw': int},
        }
        """
        summary: dict[str, dict[str, int]] = {
            'minimax':   {'first': 0, 'second': 0, 'draw': 0},
            'alphabeta': {'first': 0, 'second': 0, 'draw': 0},
            'total':     {'first': 0, 'second': 0, 'draw': 0},
        }
        for game in self.games:
            algo:   str = game['algo']
            result: str = game['result']
            if algo in summary:
                summary[algo][result] += 1
            summary['total'][result] += 1
        return summary

    def export_csv(self, filepath: str) -> None:
        """
        Export all recorded games to a CSV file.

        Parameters
        ----------
        filepath : path to write the CSV (e.g. 'results.csv')
        """
        if not self.games:
            print('No experiments to export.')
            return

        fieldnames: list[str] = [
            'game', 'algo', 'depth', 'length', 'starter',
            'result', 'nodes', 'avg_ms', 'points', 'bank'
        ]

        with open(filepath, 'w', newline='') as f:
            writer = csv.DictWriter(f, fieldnames=fieldnames)
            writer.writeheader()
            writer.writerows(self.games)

        print(f'Exported {len(self.games)} games to {os.path.abspath(filepath)}')

    def __len__(self) -> int:
        return len(self.games)

    def __repr__(self) -> str:
        return f'ExperimentTracker({len(self.games)} games recorded)'
